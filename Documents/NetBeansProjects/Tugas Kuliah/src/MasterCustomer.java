    private void txtcaridataKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcaridataKeyPressed
        Object header[]={"Kode Customer","Nama Customer","Alamat Customer","Telepon Customer","Fax Customer","Email Customer","Contact Customer"};
        DefaultTableModel data = new DefaultTableModel(null, header);
        dgv.setModel(data);
        try{
            Connection c = conn.getKoneksi();
            Statement s = c.createStatement();
            String sql = "select * from tblcustomer where nama_customer like '%"+txtcaridata.getText()+"%'";
            ResultSet res = s.executeQuery(sql);
            while(res.next()){
                String kolom1 = res.getString("kode_customer");
                String kolom2 = res.getString("nama_customer");
                String kolom3 = res.getString("alamat_customer");
                String kolom4 = res.getString("telepon_customer");
                String kolom5 = res.getString("fax_customer");
                String kolom6 = res.getString("email_customer");
                String kolom7 = res.getString("contact_customer");
                
                String kolom[] = {kolom1, kolom2, kolom3, kolom4, kolom5, kolom6, kolom7};
                data.addRow(kolom);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_txtcaridataKeyPressed

